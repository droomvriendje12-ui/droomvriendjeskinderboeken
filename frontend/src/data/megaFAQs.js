// Flatten all FAQs for schema markup
export const getAllFAQs = () => {
  const allFAQs = [];
  Object.values(megaFAQs).forEach(category => {
    category.forEach(faq => {
      allFAQs.push({
        "@type": "Question",
        "name": faq.q,
        "acceptedAnswer": {
          "@type": "Answer",
          "text": faq.a
        }
      });
    });
  });
  return allFAQs;
};

// Get FAQ count
export const getFAQCount = () => {
  let count = 0;
  Object.values(megaFAQs).forEach(category => {
    count += category.length;
  });
  return count;
};

// Get FAQs by category
export const getFAQsByCategory = (categoryKey) => {
  return megaFAQs[categoryKey] || [];
};

// Search FAQs
export const searchFAQs = (query) => {
  const results = [];
  const lowerQuery = query.toLowerCase();
  Object.values(megaFAQs).forEach(category => {
    category.forEach(faq => {
      if (faq.q.toLowerCase().includes(lowerQuery) || faq.a.toLowerCase().includes(lowerQuery)) {
        results.push(faq);
      }
    });
  });
  return results;
};

export default megaFAQs;
